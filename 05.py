import numpy as np
from scipy import stats

# 1. –ö–æ–≥–¥–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∫—Ä–∏—Ç–µ—Ä–∏–π –°—Ç—å—é–¥–µ–Ω—Ç–∞, –∞ –∫–æ–≥–¥–∞ Z ‚Äì–∫—Ä–∏—Ç–µ—Ä–∏–π?

print("–ó–∞–¥–∞–Ω–∏–µ 1")
print(
    "–ö—Ä–∏—Ç–µ—Ä–∏–π –°—Ç—å—é–¥–µ–Ω—Ç–∞ –∏—Å–ø–æ–ª—å–∑—É—é—Ç –∫–æ–≥–¥–∞ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–∞ ùûº(—Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ) –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏. –ê Z-–∫—Ä–∏—Ç–µ—Ä–∏–π - "
    "–∫–æ–≥–¥–∞ ùûº –≥–µ–Ω–µ—Ä–∞–ª—å–Ω–æ–π —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ –∏–∑–≤–µ—Å—Ç–Ω–∞.")
print()

# 2. –ü—Ä–æ–≤–µ–¥–∏—Ç–µ —Ç–µ—Å—Ç –≥–∏–ø–æ—Ç–µ–∑—ã. –£—Ç–≤–µ—Ä–∂–¥–∞–µ—Ç—Å—è, —á—Ç–æ —à–∞—Ä–∏–∫–∏ –¥–ª—è –ø–æ–¥—à–∏–ø–Ω–∏–∫–æ–≤, –∏–∑–≥–æ—Ç–æ–≤–ª–µ–Ω–Ω—ã–µ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–º —Å—Ç–∞–Ω–∫–æ–º, –∏–º–µ—é—Ç —Å—Ä–µ–¥–Ω–∏–π –¥–∏–∞–º–µ—Ç—Ä 17 –º–º.
# –ò—Å–ø–æ–ª—å–∑—É—è –æ–¥–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–∏–π –∫—Ä–∏—Ç–µ—Ä–∏–π —Å Œ±=0,05, –ø—Ä–æ–≤–µ—Ä–∏—Ç—å —ç—Ç—É –≥–∏–ø–æ—Ç–µ–∑—É, –µ—Å–ª–∏ –≤ –≤—ã–±–æ—Ä–∫–µ –∏–∑ n=100 —à–∞—Ä–∏–∫–æ–≤ —Å—Ä–µ–¥–Ω–∏–π –¥–∏–∞–º–µ—Ç—Ä
# –æ–∫–∞–∑–∞–ª—Å—è —Ä–∞–≤–Ω—ã–º 17.5 –º–º, –∞ –¥–∏—Å–ø–µ—Ä—Å–∏—è –∏–∑–≤–µ—Å—Ç–Ω–∞ –∏ —Ä–∞–≤–Ω–∞ 4 –∫–≤. –º–º.

z_nabl = (17.5 - 17) / (2 / np.sqrt(100))
alpha2 = 0.05
z_tabl = stats.norm.ppf(1 - alpha2)

print("–ó–∞–¥–∞–Ω–∏–µ 2")
print("–ù–∞–±–ª—é–¥–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ", z_nabl)
print("–¢–∞–±–ª–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ", z_tabl)
print("–ù–∞–±–ª—é–¥–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –±–æ–ª—å—à–µ —Ç–∞–±–ª–∏—á–Ω–æ–≥–æ, –ø–æ—ç—Ç–æ–º—É –æ—Ç–≤–µ—Ä–≥–∞–µ–º –≥–∏–ø–æ—Ç–µ–∑—É H0 –ø—Ä–∏ —É—Ä–æ–≤–Ω–µ –∑–Ω–∞—á–∏–º–æ—Å—Ç–∏ 0.05")
print()

# 3. –ü—Ä–æ–≤–µ–¥–∏—Ç–µ —Ç–µ—Å—Ç –≥–∏–ø–æ—Ç–µ–∑—ã. –ü—Ä–æ–¥–∞–≤–µ—Ü —É—Ç–≤–µ—Ä–∂–¥–∞–µ—Ç, —á—Ç–æ —Å—Ä–µ–¥–Ω–∏–π –≤–µ—Å –ø–∞—á–∫–∏ –ø–µ—á–µ–Ω—å—è —Å–æ—Å—Ç–∞–≤–ª—è–µ—Ç 200 –≥.
# –ò–∑ –ø–∞—Ä—Ç–∏–∏ –∏–∑–≤–ª–µ—á–µ–Ω–∞ –≤—ã–±–æ—Ä–∫–∞ –∏–∑ 10 –ø–∞—á–µ–∫. –í–µ—Å –∫–∞–∂–¥–æ–π –ø–∞—á–∫–∏ —Å–æ—Å—Ç–∞–≤–ª—è–µ—Ç:
# 202, 203, 199, 197, 195, 201, 200, 204, 194, 190.
# –ò–∑–≤–µ—Å—Ç–Ω–æ, —á—Ç–æ –∏—Ö –≤–µ—Å–∞ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω—ã –Ω–æ—Ä–º–∞–ª—å–Ω–æ.
# –í–µ—Ä–Ω–æ –ª–∏ —É—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ –ø—Ä–æ–¥–∞–≤—Ü–∞, –µ—Å–ª–∏ —É—á–∏—Ç—ã–≤–∞—Ç—å, —á—Ç–æ –¥–æ–≤–µ—Ä–∏—Ç–µ–ª—å–Ω–∞—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ä–∞–≤–Ω–∞ 99%? (–ü—Ä–æ–≤–µ—Å—Ç–∏ –¥–≤—É—Å—Ç–æ—Ä–æ–Ω–Ω–∏–π —Ç–µ—Å—Ç.)

alpha3 = 0.01
pechene = np.array([202, 203, 199, 197, 195, 201, 200, 204, 194, 190])
t_nabl = (np.mean(pechene) - 200) / (np.std(pechene, ddof=1) / len(pechene) ** 0.5)
t_tabl = stats.t.ppf(1 - alpha3 / 2, len(pechene) - 1)

print("–ó–∞–¥–∞–Ω–∏–µ 3")
print("–ù–∞–±–ª—é–¥–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ", t_nabl)
print("–¢–∞–±–ª–∏—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ", t_tabl)
print("–ù–∞–±–ª—é–¥–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –º–µ–Ω—å—à–µ —Ç–∞–±–ª–∏—á–Ω–æ–≥–æ, –ø–æ—ç—Ç–æ–º—É –ø—Ä–∏–Ω–∏–º–∞–µ–º –≥–∏–ø–æ—Ç–µ–∑—É H0 –ø—Ä–∏ —É—Ä–æ–≤–Ω–µ –∑–Ω–∞—á–∏–º–æ—Å—Ç–∏ 0.01")
print()

# 4. –ï—Å—Ç—å –ª–∏ —Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∏ –∑–Ω–∞—á–∏–º—ã–µ —Ä–∞–∑–ª–∏—á–∏—è –≤ —Ä–æ—Å—Ç–µ –¥–æ—á–µ—Ä–µ–π?
# –†–æ—Å—Ç –º–∞—Ç–µ—Ä–µ–π 172, 177, 158, 170, 178,175, 164, 160, 169
# –†–æ—Å—Ç –≤–∑—Ä–æ—Å–ª—ã—Ö –¥–æ—á–µ—Ä–µ–π: 173, 175, 162, 174, 175, 168, 155, 170, 160

rost_materej = np.array([172, 177, 158, 170, 178, 175, 164, 160, 169])
rost_docherej = np.array([173, 175, 162, 174, 175, 168, 155, 170, 160])

# –ø—É—Å—Ç—å
alpha4 = 0.05
# –¥–ª—è –ø–∞—Ä–Ω–æ–π t-–≤—ã–±–æ—Ä–∫–∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º stats.ttest_rel
print("–ó–∞–¥–∞–Ω–∏–µ 4")
print(stats.ttest_rel(rost_materej, rost_docherej))
print("pvalue > alpha4 - —Å—Ç–∞—Ç–∏—Å—Ç–∏—á–µ—Å–∫–∏ –∑–Ω–∞—á–∏–º—ã—Ö —Ä–∞–∑–ª–∏—á–∏–π –Ω–µ—Ç")
